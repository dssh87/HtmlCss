package lotto;

import java.util.Arrays;

public class Lotto {

	public static void main(String[] args) {
		// 1~45까지 숫자배열 만들기
		int fortyFiveNum[] = new int[45];
		for(int i = 0; i < fortyFiveNum.length; i++) {
			fortyFiveNum[i] = i+1;
		}
		System.out.println(Arrays.toString(fortyFiveNum));
		
		// 웹크롤링
		
	}

}
